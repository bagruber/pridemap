const path = './src/data/parades.json'
const parades = JSON.parse(readFileSync(path, 'utf8'))

let updated = 0, skipped = 0
const unmatched = []

for (const [city, url] of Object.entries(WEBSITES)) {
  const matches = parades.filter(p => p.city === city)
  if (matches.length === 0) {
    unmatched.push(city)
  } else {
    for (const p of matches) {
      if (!p.website) {
        p.website = url
        updated++
      } else {
        skipped++
      }
    }
  }
}

writeFileSync(path, JSON.stringify(parades, null, 2))
