export function t(key, lang = 'en') {
  return TRANSLATIONS[key]?.[lang] ?? TRANSLATIONS[key]?.en ?? key
}

export function cityName(city, lang = 'en') {
  return CITY_NAMES[city]?.[lang] ?? city
}

export function indexLabelL10n(score, lang = 'en') {
  if (score >= 70) return t('friendly', lang)
  if (score >= 50) return t('moderate', lang)
  if (score >= 30) return t('restrictive', lang)
  return t('hostile', lang)
}

export function labelForDaysL10n(days, lang = 'en') {
  if (days < 0) return `${Math.abs(days)}d`
  if (days === 0) return t('today', lang)
  if (days === 1) return t('tomorrow', lang)
  if (days < 7) return `${days}d`
  if (days < 30) return `${Math.round(days / 7)}w`
  if (days < 365) return `${Math.round(days / 30.4)}mo`
  return `${Math.round(days / 365)}y`
}
